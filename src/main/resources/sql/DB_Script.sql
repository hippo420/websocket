-- 테이블 생성 SQL - MESSAGE
CREATE TABLE MESSAGE
(
    `ID`       INTEGER          NOT NULL    AUTO_INCREMENT COMMENT 'ID',
    `ROOM_ID`  INT              NOT NULL    COMMENT 'ROOM_ID',
    `SND_ID`   VARCHAR(200)     NOT NULL    COMMENT 'SND_ID',
    `MESSAGE`  VARCHAR(4000)    NOT NULL    COMMENT 'MESSAGE',
    `SND_DTM`  DATETIME         NOT NULL    COMMENT 'SND_DTM',
    `TYPE`     VARCHAR(10)      NOT NULL    COMMENT 'TYPE',
    PRIMARY KEY (ID)
);

-- 테이블 Comment 설정 SQL - MESSAGE
ALTER TABLE MESSAGE COMMENT 'MESSAGE';

