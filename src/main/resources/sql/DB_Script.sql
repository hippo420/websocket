CREATE TABLE MEMBER
(
    `USER_ID`    VARCHAR(200)    NOT NULL    COMMENT 'USER_ID',
    `PASSWORD`   VARCHAR(200)    NOT NULL    COMMENT 'PASSWORD',
    `USER_NAME`  VARCHAR(200)    NULL        COMMENT 'USER_NAME',
    `IMG_CTT`    BLOB            NULL        COMMENT 'IMG_CTT',
    PRIMARY KEY (USER_ID)
);

-- 테이블 Comment 설정 SQL - MEMBER
ALTER TABLE MEMBER COMMENT 'MEMBER';

